package bcs.day01;

public class Number {
	public static void main(String[] args) {
		System.out.println(6+2);
		System.out.println(6-2);
		System.out.println(6*2);
		System.out.println(6/2);
		System.out.println(6%2);
		
		System.out.println(Math.PI);
		System.out.println(Math.floor(Math.PI));	//내림 Math.floor()
		System.out.println(Math.ceil(Math.PI));		//올림 Math.ceil()
		
		
	}
}
